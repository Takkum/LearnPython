# 面向过程Procedure-oriented的编程方式 就是通过函数组织起数据
# 面向对象 则是将数据与功能进行组合 将其分装在"对象"中
# 类 Class 可以产生一种Type
# 对象 Object 是类的实例Instance
# 在Python里整数也是一个对象，它是int类的对象
# 但是在C Java里整数是原始内置类型

# 字段Field是 对象里用来存储数据的变量 一种是对象（实例）变量 一种是类变量
# 方法Method是 对象里实现某些功能的函数
# 字段和方法统称为 类的属性
# 在Python里 Field + Method = Class
# 类通过class关键字定义


class Person:
	pass	# 通过pass可以创建一个空代码块

p = Person()
print(p)
















































